require("chromedriver");
const { Builder, By } = require("selenium-webdriver");
const { writeFile } = require("fs").promises;

const fs = require("fs");

const companiesArray = [];

function arrayToCSV(data) {
  csv = data.map((row) => Object.values(row));
  csv.unshift(Object.keys(data[0]));
  return csv.join("\n");
}

async function getDataFromCasaDados(urls) {
  for (let i = 0; i < urls.length; i++) {
    console.log("link número ", i);

    let nome;
    let cnpj;
    let cep;
    let telefone;
    let email;
    let url = urls[i];

    let driver = new Builder().forBrowser("chrome").build();

    await driver.manage().setTimeouts({
      pageLoad: 600000,
      implicit: 5000,
    });

    await driver.get(urls[i]);

    try {
      nome = await driver
        .findElement(By.xpath("//*[text()='Razão Social']"))
        .findElement(By.xpath("./following-sibling::p"))
        .getText();
      if (nome === "") {
        throw new Error("nome vazio");
      }
    } catch {
      nome = "N/A";
    }

    try {
      cnpj = await driver
        .findElement(By.xpath("//*[text()='CNPJ']"))
        .findElement(By.xpath("./following-sibling::p"))
        .getText();
      if (cnpj === "") {
        throw new Error("cnpj vazio");
      }
    } catch {
      cnpj = "N/A";
    }

    try {
      telefone = await driver
        .findElement(By.xpath("//*[text()='Telefone']"))
        .findElement(By.xpath("./following-sibling::p"))
        .getText();
      if (telefone === "") {
        throw new Error("telefone vazio");
      }
    } catch {
      telefone = "N/A";
    }

    try {
      email = await driver
        .findElement(By.xpath("//*[text()='E-MAIL']"))
        .findElement(By.xpath("./following-sibling::p"))
        .getText();
      if (email === "") {
        throw new Error("email vazio");
      }
    } catch {
      email = "N/A";
    }

    try {
      cep = await driver
        .findElement(By.xpath("//*[text()='CEP']"))
        .findElement(By.xpath("./following-sibling::p"))
        .getText();
      if (cep === "") {
        throw new Error("cep vazio");
      }
    } catch {
      cep = "N/A";
    }

    const company = {
      nome,
      cnpj,
      cep,
      telefone,
      email,
      url,
    };

    companiesArray.push(company);

    console.log(company);

    await driver.quit();
  }

  const data = JSON.stringify(companiesArray);

  fs.writeFile("companies.json", data, (err) => {
    if (err) {
      throw err;
    }
    console.log("JSON file has been saved.");
  });

  const csvList = arrayToCSV(companiesArray);

  async function writeCSV(fileName, data) {
    try {
      await writeFile(fileName, data, "utf8");
      console.log("CSV file has been saved.");
    } catch (err) {
      console.log(err);
      process.exit(1);
    }
  }

  writeCSV("companies_sheet.csv", csvList);
}

